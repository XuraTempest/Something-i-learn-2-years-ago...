// */+-
// % <- modulo
// ++ <- increment
// -- <- decrement
// ** <- pangkat atau power

let x = 10;
console.log(x + 1.5);
console.log(x - 1);
console.log(x / 5);
console.log(x * 2);

// aritmatika khusus di js
console.log(x % 4); // sisa pembagian
console.log(x ** 2); // dikalikan nilai sejenis

// pemrograman biasa
x = x + 1
console.log(x);
x = x + 1
console.log(x);
x++; // mengkopy pertambahan diatas
x++; // mengkopy pertambahan diatas
console.log(x);

x = x - 1;
console.log(x);
x--;
console.log(x);

