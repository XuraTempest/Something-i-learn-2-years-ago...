// Operator komparasi
// == equality
// != inquality
// === strict equality
// !== strict inequality
// > lebih besari dari
// >= lebih besar sama dengan
// < lebih kecil
// <= lebih kecil sama dengan

let data_bool = 10 > 7;
console.log(data_bool);

console.log(10 > 7);
console.log(10 >= 10); // harus pas 10
console.log(7 < 10);
console.log(7 <= 10);

// persamaan
console.log(3 == 3);
console.log(3 == "3"); // akan menjadi true karena hanya melihat nilainya saja
console.log(3 != 2);
console.log(3 != 3);


console.log(3 === 3);
console.log("3" === "3");
console.log(3 === "3"); // akan menjadi false karena mengecek tipe dan nilai dari variabel tersebut

console.log(3 !== 3);
console.log("3" !== "3");
console.log(3 !== "3"); // akan menjadi true karena mengecek tipe dan nilai dari variabel tersebut


